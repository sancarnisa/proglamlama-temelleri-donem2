tek_sayilar=[3,5,7,9,11,13,15,17,19]

print(tek_sayilar[0:6]) #0'dan 6.indekste kadar olan elemanları yazdıralım.

print(tek_sayilar[2:5]) #2.indeksten 5.indekste kadar olan elemanları yazdıralım.

print(tek_sayilar[3:8]) #3.indeksten 8.indekse kadar olan elemanları yazdıralım.

print(tek_sayilar[:5])  #5.indekse kadar olan elemanları yazdıralım.

print(tek_sayilar[3:])  #3.indekse kadar olan elemanları yazdıralım.

print(tek_sayilar[0:8:2]) #0'dan 8.indekse kadar olan elemanları 2'şer 2'şer yazdıralım.

print(tek_sayilar[::3])  #3.indekse kadar olan elemanları yazdıralım.
